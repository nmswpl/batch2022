public class QuotientAndRemainder {
    public static void main(String[] args)
    {
        int x = 9, y = 6;
        int quo, rem;
        quo = x/y;
        rem = x%y;
        System.out.println("Quotient is "+ quo+ " and Remainder is " + rem);
    }
}
