//input:bottom 10

package in.co.nmsworks.exercise2;

public class MultiplicationTable6 {
    public static void main(String[] args) {
    int m,n=17;
    for(int i=1; i<=10; i++){
        m=n*i;
        System.out.println(n+ "*" +i+ "=" +m );
    }
    }
}
