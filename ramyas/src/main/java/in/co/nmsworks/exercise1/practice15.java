package in.co.nmsworks.exercise1;
//Generate Multiplication Table using for loop
public class practice15
{
    public static void main(String[] args) {
        int n = 0;
        int no = 5;
        for (int i=1;i<=10;i++)
        {
             n = no * i;
            System.out.println("5 * " +i  +" = " +n);
        }
    }
}
