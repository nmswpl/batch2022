package in.co.nmsworks.exercise2;

public class MultiplicationTable {
    //program to print the multiplication table of 17 upTo 10 numbers.
    public static void main(String[] args) {
        int num=17;
        for(int i=1;i<=10;i++){
            System.out.println(num+"*"+i+"="+num*i);

        }
    }
}
