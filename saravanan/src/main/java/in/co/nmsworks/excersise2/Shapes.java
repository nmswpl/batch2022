package in.co.nmsworks.excersise2;
public class Shapes {
    public static void main(String[] args)
    {
        int number =5;
        char a='a';
        for (int i =0; i <number; i++)
        {
            for (char j =0; j<=i ; j++)
            {
                System.out.print(a);
            }
            System.out.println();
            a++;
        }
    }
}
