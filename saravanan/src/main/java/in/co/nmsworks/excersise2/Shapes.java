package in.co.nmsworks.excersise2;



public class Shapes {
    public static void main(String[] args)
    {
        int number =5;
        char[] a ={'a','b','c','d','e','f','g','h','i','k','o','n','m'};
        for (char i =0; i <number; i++)
        {
            for (int j = 0; j <= i; j++)
            {
                System.out.print(a[i]);
            }
            System.out.println();
        }
    }
}
