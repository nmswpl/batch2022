package in.co.nmsworks.exercise2;
//Write a Program to reverse the array and print.

public class RevArray {
    public static void main(String[] args) {
        char[] arr = {'a', 'b', 'c'};

        for (int i = arr.length-1; i >= 0; i--) {

            System.out.print(arr[i]);
        }
        }
        }
