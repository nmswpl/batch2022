package in.co.nmsworks.exercise2;

//Write a program to print the multiplication table of 17 upto 10 numbers.


public class problem6 {
    public static void main(String[] args) {


        int n = 17;
        for (int i = 1; i <= 10; i++)
        {
            System.out.println(n+"*"+i+"="+n*i);
        }

    }
}
