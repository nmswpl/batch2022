//Write a program to Compute Quotient and Remainder

public class problem5 {
    public static void main(String[] args) {
        int n1 = 25, n2 = 4;

        int quotient = n1 / n2;
        int remainder = n1 % n2;

        System.out.println("Quotient = " + quotient);
        System.out.println("Remainder = " + remainder);
    }
}
