package in.co.nmsworks.exercise2;
// Multiplication table (17)
public class MultiplyTableOf17
{
    public static void main(String[] args)
    {
        int table = 17;
        for (int i = 1; i <= 10; i ++)
        {
            System.out.println(table + " * " + i + " = " + table * i);
        }
    }
}
