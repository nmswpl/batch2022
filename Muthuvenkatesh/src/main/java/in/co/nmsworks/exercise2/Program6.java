// 6. Write a program to print the multiplication table of 17 upto 10 numbers.
package in.co.nmsworks.exercise2;

public class Program6 {
    public static void main(String[] args) {
        int table = 17;
        for(int i=1;i<=10;i++){
            System.out.println(table+" x "+i+" = "+(table*i));
        }
    }
}
